class NumberSwap:
    def swapNumber():
        a = 10
        b = 20
        print(" a ::")
        print(a)
        print(" b ::")
        print(b)

        print(" ********* Values after Swap ***********")
        temp = a
        a = b
        b = temp
        print(" a ::")
        print(a)
        print(" b ::")
        print(b)
NumberSwap.swapNumber()

